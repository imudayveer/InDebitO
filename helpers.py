from flask import redirect, render_template, session
from functools import wraps


# Render message as an apology/sorry to user.
def sorry(message, code=400):
    def escape(s):
        # Escape special characters. "https://github.com/jacebrowning/memegen#special-characters"
        for old, new in [("-", "--"), (" ", "-"), ("_", "__"), ("?", "~q"),
                         ("%", "~p"), ("#", "~h"), ("/", "~s"), ("\"", "''")]:
            s = s.replace(old, new)
        return s
    return render_template("sorry.html", top=code, bottom=escape(message)), code


# Decorate routes to require login. http://flask.pocoo.org/docs/1.0/patterns/viewdecorators/
def login_required(f):
    @wraps(f)
    def decorated_function(*args, **kwargs):
        if session.get("user_id") is None:
            return redirect("/login")
        return f(*args, **kwargs)
    return decorated_function


